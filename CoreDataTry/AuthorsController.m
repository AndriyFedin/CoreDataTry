{
    [super viewDidLoad];
	// Do any additional setup after loading the view, typically from a nib.
    
    Author *author = [NSEntityDescription insertNewObjectForEntityForName:@"Author" inManagedObjectContext:[CoreDataLayer getInstance].managedObjectContext];
    author.name = @"bob";
    [[CoreDataLayer getInstance].managedObjectContext save:nil];
    
    NSFetchRequest *fetchRequest = [[NSFetchRequest alloc] initWithEntityName:@"Author"];
    fetchRequest.sortDescriptors = [NSArray arrayWithObject:[NSSortDescriptor sortDescriptorWithKey:@"name" ascending:YES]];
    //fetchRequest.predicate = [NSPredicate predicateWithFormat:@"name.length < 4"];
    NSLog(@"result = %@", [[CoreDataLayer getInstance].managedObjectContext executeFetchRequest:fetchRequest error:nil]);
